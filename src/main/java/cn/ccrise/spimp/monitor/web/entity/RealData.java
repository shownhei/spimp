package cn.ccrise.spimp.monitor.web.entity;

public class RealData {
	private String count;
	private String simulateCount;
	private String switchCount;
	private String ch4Data;
	private String ch4Place;
	private String coData;
	private String coPlace;

	public String getCount() {
		return count;
	}

	public void setCount(String count) {
		this.count = count;
	}

	public String getSimulateCount() {
		return simulateCount;
	}

	public void setSimulateCount(String simulateCount) {
		this.simulateCount = simulateCount;
	}

	public String getSwitchCount() {
		return switchCount;
	}

	public void setSwitchCount(String switchCount) {
		this.switchCount = switchCount;
	}

	public String getCh4Data() {
		return ch4Data;
	}

	public void setCh4Data(String ch4Data) {
		this.ch4Data = ch4Data;
	}

	public String getCh4Place() {
		return ch4Place;
	}

	public void setCh4Place(String ch4Place) {
		this.ch4Place = ch4Place;
	}

	public String getCoData() {
		return coData;
	}

	public void setCoData(String coData) {
		this.coData = coData;
	}

	public String getCoPlace() {
		return coPlace;
	}

	public void setCoPlace(String coPlace) {
		this.coPlace = coPlace;
	}
}
